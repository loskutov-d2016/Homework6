    public String WeatherResponse () throws IOException {
        String selectedCity = ApplicationGlobalState.getInstance().getSelectedCity();

        HttpUrl detectLocationURL = new HttpUrl.Builder()
                .scheme("http")
                .host(BASE_HOST)
                .addPathSegment("locations")
                .addPathSegment(API_VERSION)
                .addPathSegment("cities")
                .addPathSegment("autocomplete")
                .addQueryParameter("apikey", API_KEY)
                .addQueryParameter("q", selectedCity)
                .build();

        Request request = new Request.Builder()
                .addHeader("accept", "application/json")
                .url(detectLocationURL)
                .build();

        Response response = client.newCall(request).execute();

        if (!response.isSuccessful()) {
            throw new IOException("Невозможно прочесть информацию о городе. " +
                    "Код ответа сервера = " + response.code() + " тело ответа = " + response.body().string());
        }
        String jsonResponse = response.body().string();
        if (objectMapper.readTree(jsonResponse).size() > 0) {
            String cityName = objectMapper.readTree(jsonResponse).get(0).at("/LocalizedName").asText();
            String Time = objectMapper.readTree(jsonResponse).get(0).at("/LocalObservationDateTime").asText();
            String Weather = objectMapper.readTree(jsonResponse).get(0).at("/WeatherText").asText();
            String Temperature = objectMapper.readTree(jsonResponse).get(0).at("/Temperature/Metric").asText();
            System.out.println("Найден город:" + cityName);
            System.out.println("На дату:" + Time);
            System.out.println("Ожидается:" + Weather);
            System.out.println("Температура:" + Temperature);

        } else throw new IOException("Server returns 0 cities");

        return objectMapper.readTree(jsonResponse).get(0).at("/Key").asText();
    }

